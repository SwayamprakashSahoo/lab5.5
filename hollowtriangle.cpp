//program to print a hollow right triangle of rows as input by the user
//including library
#include <iostream>
using namespace std;
//declaring main function
int main()
{
    int i;
    cout<<"Enter number of rows: ";
    cin>>i;
    //the program begins
    for(int j=1; j<=i; j++)
    {
        for(int k=1; k<=j; k++)
        {
            if(k==1 || k==j || j==i)
                cout<<"*";
            else
                cout<<" ";
        }
        cout<<endl;
    }
    return 0;
}
